// 1. var, let, constの使用について

// var: 変数の再代入、再宣言が行える。
var num = 1;
num = 2;  // 再代入：可
var num = 3; // 再宣言：可

// let: 変数の再代入はできるが、再宣言はできない。
let num = 1;
num = 2; // 再代入：可
let num = 3; // 再宣言：不可

// const: 変数の再代入・再宣言ができない。定数。
const num = 1;
num = 2; // 再代入：不可
const num = 3; // 再宣言：不可


// 2. varを使わない理由
// 1.変数を簡単に書き換えられてしまうと、意図しないバグが発生するため。
// 2.letやconstに比べて、varは巻き上げ時のバグを生み出しやすいため。

// 3. 定数とは
// 「const」ブロックスコープを持つ。再代入による変更や再宣言ができない。
// 代入した配列やオブジェクトに新しい要素を追加することや、代入した値の削除は可能。